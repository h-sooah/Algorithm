#include <iostream>

using namespace std;

int main() {
    
    int a, b;
    
    // 입력
    cin >> a >> b;
    
    // 출력
    cout << a + b << "\n";
    cout << a - b << "\n";
    cout << a * b << "\n";
    cout << a / b << "\n";
    cout << a % b << "\n";
    
    return 0;
    
}